						<h2 class="title">Call for Papers</h2>						
						<h5 style="text-align: justify;">
							LightSec is a crypto workshop aiming at promoting 
							and initiating novel research on the security &amp; 
							privacy issues for applications that can be termed 
							as lightweight security, due to the associated 
							constraints on metrics such as available power, 
							energy, computing ability, area, execution time, and 
							memory requirements. As such applications are 
							becoming ubiquitous, definitely providing an immense 
							value to the society, they are also affecting a 
							greater portion of the public &amp; leading to a 
							plethora of economical &amp; security and privacy 
							related concerns. The goal of this workshop is to 
							create a platform where these concerns can be 
							addressed and proposed solutions are discussed and 
							evaluated.<br>The sixth International Workshop on 
							Lightweight Cryptography for Security &amp; Privacy is 
							organized by Manipal University Jaipur, Rajasthan, 
							India.
						</h5>
