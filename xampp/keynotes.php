<p style="text-align: left;"><strong>Title</strong></p>
<div style="text-align: left;">&nbsp;Software Defined Perimeter : &quot;A new&nbsp; 
	way to protact Network Systems aginst Cyber Threats&quot;</div>
<div style="text-align: left;">&nbsp;</div>
<div style="text-align: left;"><strong>Abstract</strong></div>
<div style="text-align: left;">&nbsp;</div>
<div style="text-align: left;">Due to large variety of devices present in 
	computer network systems, cyber security plays a major role to secure and 
	improve the network or system performances. In current days, cyber security 
	is gaining lots of global interest and still remains an open research space. 
	If we follow the traditional TCP/IP based security solutions, users using 
	their devices communicate first and then are authenticated. In such 
	scenario, attackers get a chance to enter into the data transmission stage 
	before authentication process even takes place. To overcome from this 
	situation, Cloud Security Alliance came up with a novel idea called 
	&quot;Software Defined Perimeter (SDP)&quot; to authenticate first before 
	communication happens. SDP is designed with three major elements. First, a 
	security model is used to verify identity of the devices or users, and roles 
	for access before granting devices to be connected to the protected systems. 
	Second, verification using crypto based scheme is used to ensure that, the 
	security model is being followed. Finally, the security solution to achieve 
	above two issues needs to be proven in public domain security controls. In 
	this talk we explore above methods using various examples and present 
	strategies to combat future threats using SDP.</div>
