<p style="text-align: left;"><strong>Title</strong></p>
<div style="text-align: left;">&nbsp;Software Security Headaches: Analgesic or Hospital?</div>
<div style="text-align: left;">&nbsp;</div>
<strong>Abstract</strong></div>
<div style="text-align: left;">&nbsp;</div>
<div style="text-align: justify;">
&nbsp;The talk will be about analysis of complex security problems in large software - the problems where analgesic will not get rid of the headache. The need for automated analysis is clear given the enormous software. More than 100 feet stack of paper is needed to print software like the Android operating system.  Researchers are grappling with automation, from machine learning to formal verification, to analyze software. The practitioners are in a quandary what research to believe.
Patching security holes with reactive automation is not the solution when we face zero-day software defects with deadly power for catastrophic destruction. Not only must automated software security analysis scale to large software but also be proactive and highly accurate.  
This talk will reflect on: what should be the automation for software security analysis and why? It will be reflections based on years of experience of analyzing software with millions of lines of code, and leading large research projects to develop automated software analysis tools for cybersecurity.

<div style="text-align: left;">&nbsp;<strong>Bio</strong></div>
<div style="text-align:  justify;">&nbsp;Suresh Kothari is the Richardson Professor of Electrical and Computer Engineering (ECE) at Iowa State University (ISU). He has pioneered research on machine-enabled reasoning to solve complex problems of software productivity, security and safety. <br>He served as a Principal Investigator (PI) for the US Defense Advanced Research Project Agency (DARPA) Automated Program Analysis for Cybersecurity (APAC) program, and a Co-PI for the DARPA Software Enabled Control (SEC) program. Currently he is a PI for the DARPA Space/Time Analysis for Cybersecurity (STAC) program. </br> EnSoft (http://www.ensoftcorp.com/), the company he founded in 2002, provides software productivity, safety, and security products and services worldwide to more than 330 organizations including all major avionics and automobile companies. He was awarded in 2012 the Iowa State Board of Regents Professor Award for excellence in research, teaching, and service. He has served as a Distinguished ACM Lecturer. He has given more than 100 invited talks worldwide at major conferences, government organizations, universities, and industry.</div>
